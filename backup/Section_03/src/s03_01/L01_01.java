package s03_01;

/**
 * 리터럴 (Literal)
 * 리터럴 또는 리터럴 상수는 값 자체를 의미한다.
 * ✅ Java에서는 리터럴 상수를 단독으로 선언할 수 없다.
 */

public class L01_01 {
    public static void main(String[] args) {
        // 리터럴은 값의 형태에 따라 5가지로 나눌 수 있다.
        // 1) 정수 리터럴 (Integer Literal)
        System.out.println(32);
        // 2) 부동소수점 리터럴 (Floating-point Literal)
        System.out.println(175.6);
        // 3) 논리 리터럴 (Boolean Literal)
        System.out.println(true);
        // 4) 문자 리터럴 (Character Literal)
        System.out.println('M');
        // 5) 문자열 리터럴 (String Literal)
        System.out.println("Thomas");
    }
}
