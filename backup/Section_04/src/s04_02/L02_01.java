package s04_02;

/**
 * 부호 연산자 (Sign Operator)
 * 정수형 또는 실수형 리터럴의 부호를 표시하는 연산자
 */

public class L02_01 {
    public static void main(String[] args) {
        // +a	    피연산자의 부호가 (+)임을 나타낸다.
        int num = +10;
        System.out.println(num);
        // -a	    피연산자의 부호가 (-)임을 나타낸다.
        num = -10;
        System.out.println(num);
    }
}
