package s03_01;

/**
 * 부동소수점 리터럴 (Floating-point Literal)
 * 부동소수점 형태의 값 자체를 의미한다.
 */

public class L01_03 {
    public static void main(String[] args) {
        // 부동소수점 리터럴의 표기
        double _base_10 =   12.2;     // 10진수 표기
        double _base_16 = 0x12.2p2;   // 부동소수점 표기
        // 부동소수점 리터럴의 자료형
        double _8b_double = 1.0;      // double형 부동소수점 리터럴 1.0
        float  _4b_float  = 1.0f;     //  float형 부동소수점 리터럴 1.0
    }
}
