package s03_02;

/**
 * 문자형 (Character type)
 * 문자 하나를 다루는 자료형
 */

public class L02_05 {
    public static void main(String[] args) {
        // char     2[byte]     0 ~ 65,535
        char _char;
        _char = Character.MIN_VALUE;
        _char = Character.MAX_VALUE;
        // Java에서는 문자 하나당 16[bit]를 할당하는 UTF_16 방식을 사용한다.
    }
}
